class ChileLocationService {
  async getRegions(): Promise<Region[]> {
    // Return static regions data
    return Promise.resolve([...REGIONS]);
  }

  async getCitiesByRegion(regionId: string): Promise<City[]> {
    // Return static cities data for the given region
    return Promise.resolve([...(CITIES_BY_REGION[regionId] || [])]);
  }

  // Method to get all regions and cities at once (for better UX)
  async getAllRegionsWithCities(): Promise<{ regions: Region[], citiesByRegion: { [key: string]: City[] } }> {
    const regions = await this.getRegions();
    const citiesByRegion: { [key: string]: City[] } = {};

    // Copy all cities data
    Object.keys(CITIES_BY_REGION).forEach(regionId => {
      citiesByRegion[regionId] = [...CITIES_BY_REGION[regionId]];
    });

    return { regions, citiesByRegion };
  }
}

export const chileLocationService = new ChileLocationService();
